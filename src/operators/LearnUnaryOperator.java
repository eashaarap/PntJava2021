package operators;

public class LearnUnaryOperator {

    /*
        Increment Operator = ++ ( adds 1)
            PreIncrement: ++number
            PostIncrement: number++
        Decrement Operator = -- ( decreases 1)
            PreDecrement: --number
            PostIncrement: number--
     */


    public static void main(String[] args) {

        int number = 30;
        ++number;// 1+ 30
        number++;//31 + 1 for future
        number++;
        ++number;
        --number;
        System.out.println(number);








    }




}
